#!/usr/bin/env python3
"""Aggregate a directory of run-benchmark.sh JSON files into SUMMARY.md + results.csv.

Two modes, auto-detected from the filenames in the results directory:

* reproduction  -- files named ``<ID>-v1.json`` / ``<ID>-v2.json`` (B1-B3): emits the
                   paired v1-vs-v2 comparison tables used in the thesis evaluation.
* flat          -- one JSON per fixture (stress / general suites): emits a single
                   per-fixture table with event counts, expansion ratio, and stage timing.

Usage:
    aggregate.py <results-dir> [--title "Stress suite"]
"""
from __future__ import annotations

import argparse
import csv
import json
import re
from pathlib import Path

REPRO_RE = re.compile(r"^(?P<id>.+)-(?P<ver>v[12])\.json$")


def load(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def fmt(value: object, spec: str = "") -> str:
    if value is None:
        return "-"
    if spec:
        return format(value, spec)
    return str(value)


def write_csv(rows: list[dict], path: Path) -> None:
    fieldnames: list[str] = []
    for row in rows:
        for key in row:
            if key not in fieldnames:
                fieldnames.append(key)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def stage(data: dict, key: str) -> float | None:
    return (data.get("stages_mean_ms") or {}).get(key)


def aggregate_reproduction(results_dir: Path, title: str) -> None:
    ids: list[str] = []
    for path in sorted(results_dir.glob("*-v1.json")):
        match = REPRO_RE.match(path.name)
        if match:
            ids.append(match.group("id"))

    rows: list[dict] = []
    for bench_id in ids:
        for ver in ("v1", "v2"):
            data = load(results_dir / f"{bench_id}-{ver}.json")
            rows.append(
                {
                    "benchmark": bench_id,
                    "version": ver,
                    "mean_ms": data["mean_ms"],
                    "stdev_ms": data["stdev_ms"],
                    "midi_bytes": data["midi_bytes"],
                    "parse_ms": stage(data, "parse_ms"),
                    "semantic_ms": stage(data, "semantic_ms"),
                    "lowering_ms": stage(data, "lowering_ms"),
                    "midi_ms": stage(data, "midi_ms"),
                }
            )
    write_csv(rows, results_dir / "results.csv")

    def pick(bench_id: str, ver: str) -> dict:
        return next(r for r in rows if r["benchmark"] == bench_id and r["version"] == ver)

    lines = [
        f"# {title}",
        "",
        "> Generated by `aggregate.py`. Wall clock = Python `time.perf_counter` around the",
        "> compiler subprocess (5 warmup + 50 measured runs); per-stage = `MOTIVO_BENCHMARK=1`.",
        "",
        "## End-to-end wall clock (ms)",
        "",
        "| Benchmark | v1 mean ± σ | v2 mean ± σ | Δ v2−v1 (ms) | v2/v1 |",
        "|-----------|-------------|-------------|--------------|-------|",
    ]
    for bench_id in ids:
        v1, v2 = pick(bench_id, "v1"), pick(bench_id, "v2")
        delta = v2["mean_ms"] - v1["mean_ms"]
        ratio = v2["mean_ms"] / v1["mean_ms"] if v1["mean_ms"] else float("nan")
        lines.append(
            f"| {bench_id} | {v1['mean_ms']:.4f} ± {v1['stdev_ms']:.4f} | "
            f"{v2['mean_ms']:.4f} ± {v2['stdev_ms']:.4f} | {delta:+.4f} | {ratio:.4f} |"
        )

    lines += ["", "## MIDI output size (bytes)", "",
              "| Benchmark | v1 | v2 | Match |", "|-----------|----|----|-------|"]
    for bench_id in ids:
        v1, v2 = pick(bench_id, "v1"), pick(bench_id, "v2")
        match = "yes" if v1["midi_bytes"] == v2["midi_bytes"] else "**no**"
        lines.append(f"| {bench_id} | {v1['midi_bytes']} | {v2['midi_bytes']} | {match} |")

    lines += ["", "## Per-stage mean (ms, in-process)", "",
              "| Benchmark | Ver | parse | semantic | lowering | midi write |",
              "|-----------|-----|-------|----------|----------|------------|"]
    for bench_id in ids:
        for ver in ("v1", "v2"):
            r = pick(bench_id, ver)
            lines.append(
                f"| {bench_id} | {ver} | {fmt(r['parse_ms'])} | {fmt(r['semantic_ms'])} | "
                f"{fmt(r['lowering_ms'])} | {fmt(r['midi_ms'])} |"
            )
    lines.append("")
    (results_dir / "SUMMARY.md").write_text("\n".join(lines), encoding="utf-8")
    print((results_dir / "SUMMARY.md").read_text(encoding="utf-8"))


def aggregate_flat(results_dir: Path, title: str) -> None:
    paths = sorted(p for p in results_dir.glob("*.json"))
    rows: list[dict] = []
    for path in paths:
        data = load(path)
        rows.append(
            {
                "fixture": path.stem,
                "note_events": data.get("note_events"),
                "source_loc": data.get("source_loc"),
                "events_per_loc": data.get("events_per_loc"),
                "mean_ms": data["mean_ms"],
                "stdev_ms": data["stdev_ms"],
                "parse_ms": stage(data, "parse_ms"),
                "semantic_ms": stage(data, "semantic_ms"),
                "lowering_ms": stage(data, "lowering_ms"),
                "midi_ms": stage(data, "midi_ms"),
                "midi_bytes": data.get("midi_bytes"),
            }
        )
    # Sort by event count where available, so scaling sweeps read in order.
    rows.sort(key=lambda r: (r["note_events"] is None, r["note_events"] or 0, r["fixture"]))
    write_csv(rows, results_dir / "results.csv")

    lines = [
        f"# {title}",
        "",
        "> Generated by `aggregate.py`. Compiler: current `motivoc` (Release).",
        "> Wall clock = `time.perf_counter` around the subprocess (5 warmup + 50 measured);",
        "> per-stage and event count via `MOTIVO_BENCHMARK=1`.",
        "",
        "| Fixture | Events | LOC | Ev/LOC | mean ms ± σ | parse | semantic | lowering | midi | MIDI B |",
        "|---------|-------:|----:|-------:|-------------|------:|---------:|---------:|-----:|-------:|",
    ]
    for r in rows:
        lines.append(
            f"| {r['fixture']} | {fmt(r['note_events'])} | {fmt(r['source_loc'])} | "
            f"{fmt(r['events_per_loc'])} | {r['mean_ms']:.3f} ± {r['stdev_ms']:.3f} | "
            f"{fmt(r['parse_ms'])} | {fmt(r['semantic_ms'])} | {fmt(r['lowering_ms'])} | "
            f"{fmt(r['midi_ms'])} | {fmt(r['midi_bytes'])} |"
        )
    lines.append("")
    (results_dir / "SUMMARY.md").write_text("\n".join(lines), encoding="utf-8")
    print((results_dir / "SUMMARY.md").read_text(encoding="utf-8"))


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("results_dir", type=Path)
    parser.add_argument("--title", default=None)
    args = parser.parse_args()

    results_dir: Path = args.results_dir
    is_reproduction = any(results_dir.glob("*-v1.json"))
    title = args.title or f"{results_dir.name.title()} Results Summary"

    if is_reproduction:
        aggregate_reproduction(results_dir, title)
    else:
        aggregate_flat(results_dir, title)


if __name__ == "__main__":
    main()
